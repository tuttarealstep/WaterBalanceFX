
public class TableRow {
    
    private int gdd;
    private double kc;
    
    public TableRow(int gdd, double kc) {
        
        this.gdd = gdd;
        this.kc = kc;
        
    }

    public int getGdd() {
        return gdd;
    }

    public void setGdd(int gdd) {
        this.gdd = gdd;
    }

    public double getKc() {
        return kc;
    }

    public void setKc(double kc) {
        this.kc = kc;
    }
    
    
}
